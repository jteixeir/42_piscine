#include <unistd.h>

void	ft_putchar(int i)
{
	write(1, &i, 1);
}

void	ft_putnbr(int nb)
{
	long x;

	x = nb;
	if (x < 0)
	{
		write(1, "-", 1);
		x = x * -1;
	}
	if (x / 10 > 0)
	{
		ft_putnbr(x / 10);
	}
	ft_putchar((x % 10) + 48);
}
