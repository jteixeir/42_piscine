char	*ft_strstr(char *str, char *to_find)
{
	int c1;
	int c2;

	c1 = 0;
	if (to_find[0] == '\0')
		return (&str[c1]);
	while (str[c1] != '\0')
	{
		c2 = 0;
		while ((to_find[c2] == str[c1 + c2]) && (str[c1 + c2]))
		{
			if (to_find[c2 + 1] == '\0')
				return (&str[c1]);
			c2++;
		}
		c1++;
	}
	return (0);
}
